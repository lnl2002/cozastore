package model;

/**
 *
 * @author Nhat Anh
 */
public class ProductCart {
    int cartId;
    int productId;
    String nameProduct;
    int amount;
    double price;
    String image;
    String size;

    public ProductCart() {
    }

    public ProductCart(int cartId, int productId, String nameProduct, int amount, double price,String image,String size) {
        this.cartId = cartId;
        this.productId = productId;
        this.nameProduct = nameProduct;
        this.amount = amount;
        this.price = price;
        this.image=image;
        this.size=size;
    }

    public int getCartId() {
        return cartId;
    }

    public void setCartId(int cartId) {
        this.cartId = cartId;
    }

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

    public String getNameProduct() {
        return nameProduct;
    }

    public void setNameProduct(String nameProduct) {
        this.nameProduct = nameProduct;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
    
}
